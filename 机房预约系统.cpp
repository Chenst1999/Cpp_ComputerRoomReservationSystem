//登录功能
void LoginIn(string fileName, int type) {
	Identity* person = NULL;//父类指针，用于指向子类对象
	//读文件
	ifstream ifs;
	ifs.open(fileName, ios::in);
	if (!ifs.is_open()) {
		cout << "文件不存在" << endl;
		ifs.close();
		return;
	}
	//准备接受用户的信息
	int id = 0;
	string name;
	string pwd;
	//判断身份
	if (type == 1) {//学生
		cout << "请输入你的学号：" << endl;
		cin >> id;
	}
	else if (type == 2) {//老师
		cout << "请输入你的职工号：" << endl;
		cin >> id;
	}
	cout << "请输入用户名：" << endl;
	cin >> name;
	cout << "请输入密码：" << endl;
	cin >> pwd;
	if (type == 1) {//学生身份验证
		int fid;
		string fname;
		string fpwd;
		while (ifs>>fid&&ifs>>fname&&ifs>>fpwd) {
			//与用户所输入的信息做对比
			if (fid == id&&fname == name&&fpwd == pwd) {
				cout << "学生验证登录成功" << endl;
				system("pause");
				system("cls");
				person = new Student(id, name, pwd);
				//进入学生子菜单
				studentMenu(person);
				return;
			}
		}
	}
	else if (type == 2) {//老师身份验证
		int fid;
		string fname;
		string fpwd;
		while (ifs >> fid && ifs >> fname && ifs >> fpwd) {
			//与用户所输入的信息做对比
			if (fid == id && fname == name && fpwd == pwd) {
				cout << "老师验证登录成功" << endl;
				system("pause");
				system("cls");
				person = new Teacher(id, name, pwd);
				//进入教师子菜单
				teacherMenu(person);
				return;
			}
		}
	}
	else if (type == 3) {//管理员身份验证
		string fname;
		string fpwd;
		while (ifs >> fname && ifs >> fpwd) {
			//与用户所输入的信息做对比
			if (fname == name && fpwd == pwd) {
				cout << "管理员验证登录成功" << endl;
				system("pause");
				system("cls");
				person = new Manager(name, pwd);
				//进入管理员子菜单
				managerMenu(person);
				return;
			}
		}
	}
	cout << "验证登录失败！" << endl;
	system("pause");
	system("cls");
	return;
}
